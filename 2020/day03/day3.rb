require "debug"

data = DATA.readlines.map(&:chomp).map(&:chars)

curr_row = 0
curr_col = 0
count = 0

loop do
  curr_row += 1
  curr_col += 3
  curr_col %= data[0].size

  curr_pos = data.dig(curr_row, curr_col)
  break if curr_pos.nil?

  count += 1 if curr_pos == "#"
end

puts count

# part 2

slopes = [
  [1, 1],
  [3, 1],
  [5, 1],
  [7, 1],
  [1, 2]
]

trees = []

slopes.each do |(col, row)|
  count = 0
  curr_row = 0
  curr_col = 0
  loop do
    curr_row += row
    curr_col += col
    curr_col %= data[0].size

    curr_pos = data.dig(curr_row, curr_col)
    if curr_pos.nil?
      trees << count
      break
    end

    count += 1 if curr_pos == "#"
  end
end

puts trees.inject(&:*)

